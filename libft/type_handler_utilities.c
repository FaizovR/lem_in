#include "ft_printf_main.h"

int		ft_is_conversion(int c)
{
	return ((c == 'c') || (c == 's') || (c == 'p') || (c == 'd') || (c == 'i')
		|| (c == 'o') || (c == 'u') || (c == 'x') || (c == 'X')
		|| (c == 'f') || (c == '%') || (c == 'b') || (c == 'e') || (c == 'g'));
}

int		ft_is_flag(int c)
{
	return ((c == ' ') || (c == '-') || (c == '+') || (c == '#') || (c == '\'')
	|| (c == '.') || (c == '$'));
}
